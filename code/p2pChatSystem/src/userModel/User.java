package userModel;

public class User {

	/** Identificateur unique d'un User **/
	private int idUser ;
	
	/** Nom d'utilisateur du User **/
	private String nickname ; 
	
	/** Getter du idUser **/
	public int getIdUser () {
		return idUser ;
	}
	
	/** Getter du Nickname **/
	public String getNickname () {
		return nickname ;
	}
	
}
